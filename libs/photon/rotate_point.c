typedef struct _quadrants_t
  {
  float s0, s1;
  float c0, c1;
  } quadrants_t;

// first 2 are sin conversions
// second are cos conversion
static const quadrants_t quadrants[4] =
  {
    { 1.0, 0.0, 0.0, 1.0 },     // 0..89
    { 0.0, 1.0, -1.0, 0.0 },     // 90..179
    { -1.0, 0.0, 0.0, -1.0 },     // 180..269
    { 0.0, -1.0, 1.0, 0.0 }
  };

// angle is in degrees, not radians.
const point_t* _rotate_point(const point_t* center, int16_t angle, point_t* pt)
  {
  int la = angle % 90;
  int qd = angle / 90;
  // convert the angle to a usefull form
  float cos_theta = rotn_table[la][0] * quadrants[qd].c0
    + rotn_table[la][1] * quadrants[qd].c1;
  float sin_theta = rotn_table[la][0] * quadrants[qd].s0
    + rotn_table[la][1] * quadrants[qd].s1;

  // calc the transformation
  float x2f = (pt->x - center->x) * cos_theta - (pt->y - center->y) * sin_theta;
  gdi_dim_t x2 = (gdi_dim_t)(roundf(x2f));

  float y2f = (pt->x - center->x) * sin_theta + (pt->y - center->y) * cos_theta;
  gdi_dim_t y2 = (gdi_dim_t)(roundf(y2f));

  pt->x = x2 + center->x;
  pt->y = y2 + center->y;

  return pt;
  }

result_t rotate_point(const point_t* center, int16_t angle, point_t* pt)
  {
  if (center == 0 || pt == 0)
    return e_bad_parameter;

  while (angle > 360)
    angle -= 360;

  while (angle < 0)
    angle += 360;

  if (angle == 0)
    return s_ok;

  _rotate_point(center, angle, pt);

  return s_ok;
  }
